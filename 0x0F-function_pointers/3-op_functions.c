/**
 * op_add - adds twoo integers
 * @a: int 1 to add to int 2
 * @b: int 2 to add to int 1
 * Return: sum of two
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts two integers
 * @a: int 1 to be subtracted
 * @b: int 2 to sub from 1
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two integers
 * @a: int 1 to mul to int 2
 * @b: int 2 to mul to 1
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
